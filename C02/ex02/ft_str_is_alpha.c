int	ft_str_is_alpha(char *str)
{
	int	ii;

	ii = -1;
	while (str[++ii])
	{
		if (!((str[ii] >= 'A' && str[ii] <= 'Z')
				|| (str[ii] >= 'a' && str[ii] <= 'z')))
			return (0);
	}
	return (1);
}
